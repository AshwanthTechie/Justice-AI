export const getStates = () => Object.keys(locations);

export const getDistricts = (state) => state && locations[state] ? Object.keys(locations[state]) : [];

export const getCities = (state, district) => {
  const cities = state && district && locations[state] && locations[state][district] ? locations[state][district] : [];
  // If no curated city list exists for the district, fall back to the district name
  // so the EditProfile UI will still present a selectable city option.
  if (cities && cities.length) return cities;
  return district ? [district] : [];
};
